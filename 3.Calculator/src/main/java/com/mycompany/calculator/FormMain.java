    private void SetButtonClickedStyle(JButton button){
        button.setBackground(Color.WHITE);
        button.setForeground(Color.black);
        button.setFocusPainted(false);
        button.setBorder(BorderFactory.createEtchedBorder(EtchedBorder.RAISED,Color.BLACK, Color.white));
    }
    private void setButtonDefaulStyle(JButton button){
        button.setBackground(Color.DARK_GRAY);
        button.setForeground(Color.WHITE);
        button.setBorder(BorderFactory.createEtchedBorder(EtchedBorder.RAISED,Color.BLACK, Color.white));
    }
    private void ButtonStyleClicked(JButton button){
        JButton[] allButtons = {tru, cong, nhan, chia};
        for (JButton btn : allButtons) {
            if (btn == button) {
                SetButtonClickedStyle(btn);
            }else{
                setButtonDefaulStyle(btn);
            }
        }
    }
    
