    private boolean CheckNumbers(){
        String n1 = number1.getText().trim();
        String n2 = number2.getText().trim();
        if(n1.isEmpty() || n2.isEmpty()){
            result.setText("Không đủ dữ liệu !");
            result.setForeground(Color.red);
            return false;
        }
        try{
            Double.parseDouble(n1);
            Double.parseDouble(n2);
            
        }catch( Exception e){
            result.setText("Sai kiểu dữ liệu !");
            result.setForeground(Color.red);
            return false;
        }
        
        return true;
    }
