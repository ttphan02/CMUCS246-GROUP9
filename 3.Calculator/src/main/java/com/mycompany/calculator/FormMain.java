    private void chiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chiaActionPerformed
       ButtonStyleClicked(chia);
       if(CheckNumbers() ==  true){
           if(Double.parseDouble(number2.getText()) == 0.0){
            result.setText("Không thể chia cho 0 !");
            result.setForeground(Color.red);
            }else{
                double RS = Double.parseDouble(number1.getText()) / Double.parseDouble(number2.getText());
                if(RS % 1 == 0){
                   result.setText(Integer.toString((int)RS));
                   result.setForeground(Color.black);
                }else{
                    result.setText(Double.toString(RS));
                    result.setForeground(Color.black);
                } 
            }
            
        }
    }//GEN-LAST:event_chiaActionPerformed

