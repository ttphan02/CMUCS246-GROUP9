    private void truActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_truActionPerformed
        ButtonStyleClicked(tru);
        if(CheckNumbers() ==  true){
            double RS = Double.parseDouble(number1.getText()) - Double.parseDouble(number2.getText());
            if(RS % 1 == 0){
               result.setText(Integer.toString((int)RS));
               result.setForeground(Color.black);
            }else{
                result.setText(Double.toString(RS));
                result.setForeground(Color.black);
            } 
        }
    }//GEN-LAST:event_truActionPerformed

